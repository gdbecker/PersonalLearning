public class WhileStatements {

    public static void main(String[] args) {

        //
        // while loops
        int j = 1;
        while (j <= 5) {
            System.out.println("while " + j);
            j += 1;
        }

        //
        // do-while loops
        int v = 1;
        do {
            System.out.println("do-while " + v);
            v += 1;
        } while (v <= 8);
    }
}
